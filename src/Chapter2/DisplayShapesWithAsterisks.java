package Chapter2;
//2.18 (Displaying Shapes with Asterisks) Write an application that displays a box, an oval, an ar-
//        row and a diamond using asterisks ( * ), as follows:
//        *********       ***            *         *
//        *       *     *     *         ***      *   *
//        *       *    *       *       *****    *      *
//        *       *    *       *         *     *        *
//        *       *    *       *         *      *      *
//        *       *     *      *         *        *  *
//        *********       ***            *          *

public class DisplayShapesWithAsterisks {
    public static void main(String[] args) {
        for (int i = 1; i <= 1; i++) {
            System.out.print("*********\t   ***\t\t\t*\t\t  *");
            System.out.println();
            for (int j = 1; j <= 1; j++) {
                System.out.print("*\t    *\t  *\t   *\t   ***\t\t*   *");

                System.out.println();
                for (int l = 1; l <= 1; l++) {
                    System.out.print("*\t    *\t *\t    *\t  *****\t   *     *");

                    System.out.println();
                    for (int k = 1; k <= 1; k++) {
                        System.out.print("*\t    *\t *\t    *\t    *\t  *       *");

                        System.out.println();
                        for (int m = 1; m <= 1; m++) {
                            System.out.print("*\t    *\t *\t    *\t    *\t   *     *");

                            System.out.println();
                            for (int n = 1; n <= 1; n++) {
                                System.out.print("*\t    *\t  *\t   *\t    * \t\t*   *");

                                System.out.println();
                                for (int e = 1; e <= 1; e++) {
                                    System.out.print("*********\t   ***\t\t\t*\t\t  *");

                                }
                            }

                        }
                    }
                }
            }
        }
    }
}

